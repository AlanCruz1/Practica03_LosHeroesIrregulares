/**
* Interface que recibe todas las elecciones del cliente  
* de korby
* @author Villa Padilla Juan Carlos
* @version 1.0
*/
public interface Baguette{

    /** 
    * Metodo que recibe la descripcion de todos los panes y de todos los ingredientes
    */
    public String getDescripcion();

    /** 
    * Metodo que recibe el precio de todos los panes y de todos los ingredientes
    */
    public double getPrecio();

    /** 
    * Metodo que recibe la cantidad de pollo que va a utilizar el cliente en su baguette
    */
    public int getPollos();

    /** 
    * Metodo que recibe la cantidad de pepperoni que va a utilizar el cliente en su baguette
    */
    public int getPepperonis();

    /** 
    * Metodo que recibe la cantidad de jamon que va a utilizar el cliente en su baguette
    */
    public int getJamones();

    /** 
    * Metodo que recibe la cantidad de lechuga que va a utilizar el cliente en su baguette
    */
    public int getLechugas();

    /** 
    * Metodo que recibe la cantidad de jitomate que va a utilizar el cliente en su baguette
    */
    public int getJitomates();

    /** 
    * Metodo que recibe la cantidad de cebolla que va a utilizar el cliente en su baguette
    */
    public int getCebollas();

    /** 
    * Metodo que recibe la cantidad de mostaza que va a utilizar el cliente en su baguette
    */
    public int getMostazas();

    /** 
    * Metodo que recibe la cantidad de catsup que va a utilizar el cliente en su baguette
    */
    public int getCatsups();
    
    /** 
    * Metodo que recibe la cantidad de mayonesa que va a utilizar el cliente en su baguette
    */
    public int getMayonesas();

}
